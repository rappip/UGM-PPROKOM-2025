def penjumlahan(a, b):
    return a + b

def pengurangan(a, b):
    return a - b

def perkalian(a, b):
    return a * b

def pembagian(a, b):
    if b == 0:
        return "Tidak dapat dibagi dengan nol"
    return a / b

def modulo(a, b):
    return a % b

def pangkat(a, b):
    return a ** b